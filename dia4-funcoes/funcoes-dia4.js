// Função sem retorno
function saudacao(nome) {
  console.log(`Olá, ${nome}! Seja bem-vindo.`);
}

// Função com retorno
function soma(a, b) {
  return a + b;
}

// Executando
saudacao("Robson");
console.log("Soma: " + soma(5, 3));

// Função que calcula o quadrado de um número
function quadrado(x) {
  return x * x;
}
console.log("Quadrado de 4: " + quadrado(4));
